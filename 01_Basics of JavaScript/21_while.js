// Example - > 4
let biggest = 0;
let numbers = [5, 10, 2, 8, 3];

while (numbers.length > 0) {
  let currentNumber = numbers.pop();
  if (currentNumber > biggest) {
    biggest = currentNumber;
  }
}

console.log(`The biggest number is ${biggest}`);

